# Make a calculator using Python with addition , subtraction , multiplication ,division and power.

def add(a, b):
    return a + b


def sub(a, b):
    return a - b


def mul(a, b):
    return a * b;


def div(a, b):
    return a / b;


def power(no):
    return pow(no, 4)


print(add(2, 4))
print(sub(2, 4))
print(mul(2, 4))
print(div(2, 4))
print(power(3))
